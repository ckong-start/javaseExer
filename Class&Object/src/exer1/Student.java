package exer1;

public class Student {
	private String name = "god";
	private int age;
	

	public Student() {
		System.out.println(name);
	}
	

	public Student(String name) {
		this.name = name;
		
		System.out.println(this.name);
	}


	public String getName() {
		return name = "goood";
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

}
